function CreateQuestRewardSystem()
	if NpcId ~= 0 then return end
	
	-- hol dir die richtige tabelle für die jeweilige plattform
	local Chunk = "return QuestRewardsP" .. PlatformId
	local Rewards = dostring(Chunk)
	--print("Using Rewards:")
	--dump(Rewards)
	
	-- ggfs. frühzeitig aussteigen
	if not Rewards then return end

	for globalflag, reward in Rewards do
		local actions = {}
		print("Award when \"" .. globalflag .. "\" is true:")

		-- give items
		if reward.Items then
			foreachi(reward.Items, function(i, item)
				tinsert(%actions, TransferItem{GiveItem = item, Flag = Give})
				--tinsert(%actions, ShowDebugText{String = "You have been rewarded an item !" .. " ItemId = " .. item, Color = ColorMagenta})
				print("You have been rewarded an item !" .. " ItemId = " .. item)
			end)
		end
		
		-- give money
		if reward.Money then
			local gold, silver, copper = reward.Money.Gold, reward.Money.Silver, reward.Money.Copper
			if not gold then gold = 0 end
			if not silver then silver = 0 end
			if not copper then copper = 0 end
			if copper > 0 or silver > 0 or gold > 0 then
				tinsert(actions, TransferMoney{Gold = gold, Silver = silver, Copper = copper, Flag = Give})
				local moneystr = " "
				if gold > 0 then
					moneystr = moneystr .. gold .. " Gold "
				end
				if silver > 0 then
					if moneystr ~= " " then moneystr = moneystr .. "and " end
					moneystr = moneystr .. silver .. " Silver "
				end
				if copper > 0 then
					if moneystr ~= " " then moneystr = moneystr .. "and " end
					moneystr = moneystr .. copper .. " Copper "
				end
	
				--tinsert(actions, ShowDebugText{String = "You have received" .. moneystr .. "!", Color = ColorSand})
				print("You have received" .. moneystr .. "!")
			end
		end
		
		-- give XP
		if reward.XP then
			if getn(reward.XP) == 1 then
				tinsert(actions, TransferXP{XP = reward.XP[1], Flag = Give})
				--tinsert(actions, ShowDebugText{String = "You have gained " .. reward.XP[1] .. " Experience Points!", Color = ColorGreen})
				print("You have gained " .. reward.XP[1] .. " Experience Points !")
			elseif getn(reward.XP) > 1 then
				assert2(nil, "RewardSystem: Don't know how to reward more than one XP entry!")
			end
		end
		
		if PlatformId >= 200 then
			OnOneTimeEvent
			{
				Conditions = { IsRewardFlagTrue{Name = globalflag, UpdateInterval = 30},	},
				Actions = actions,
			}
		else
			OnOneTimeEvent
			{
				Conditions = { IsRewardFlagTrue{Name = globalflag, UpdateInterval = 20},	},
				Actions = actions,
			}
		end
	end
end
